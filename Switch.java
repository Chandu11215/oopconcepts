class Switch
{
public static void main(String[] args)
{
 int i=10;
 switch(i)
{
 case 0:
System.out.println("i is greater than zero");
 break;
 case 1:
System.out.println("i is greater than one");
 break;
 case 2:
System.out.println("i is greater than 9");
 break;
default:
System.out.println("i is greater than 8");
}
}
}
